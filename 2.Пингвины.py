# Количество Пингвинов

ping_number = input()

# Пингвин

ping_hair = '    _~_    '  * int(ping_number)
ping_head = '   (o o)   '  * int(ping_number)
ping_nose = '  /  V  \\  ' * int(ping_number)
ping_body = ' /(  _  )\\ ' * int(ping_number)
ping_legs = '   ^^ ^^   '  * int(ping_number)

# Вывод пингвина

print(ping_hair)
print(ping_head)
print(ping_nose)
print(ping_body)
print(ping_legs)